pub fn drawText(renderer: anytype, text: []const u8, x: i32, y: i32, scale: i32) void {
    const c = @import("sdl.zig").c;
    
    for (text, 0..) |char, char_idx| {
        if (char < 32 or char > 126) continue;
        const font_idx = (@as(usize, char) - 32) * 8;
        const start_x = x + @as(i32, @intCast(char_idx)) * 8 * scale;
        
        for (0..8) |row| {
            const bits = font_data[font_idx + row];
            for (0..8) |col| {
                if ((bits & (@as(u8, 0x80) >> @as(u3, @intCast(col)))) != 0) {
                    const rect = c.SDL_Rect{
                        .x = start_x + @as(i32, @intCast(col)) * scale,
                        .y = y + @as(i32, @intCast(row)) * scale,
                        .w = scale,
                        .h = scale,
                    };
                    _ = c.SDL_RenderFillRect(renderer, &rect);
                }
            }
        }
    }
}

pub fn drawTextCentered(renderer: anytype, text: []const u8, r: anytype, scale: i32) void {
    const text_w = @as(i32, @intCast(text.len * 8)) * scale;
    const text_h = 8 * scale;
    const tx = r.x + @divTrunc(r.w - text_w, 2);
    const ty = r.y + @divTrunc(r.h - text_h, 2);
    drawText(renderer, text, tx, ty, scale);
}

// Overload with C context if needed for retro-compatibility
pub fn drawTextWithC(_: anytype, renderer: anytype, text: []const u8, x: i32, y: i32, scale: i32) void {
    drawText(renderer, text, x, y, scale);
}

pub fn drawTextCenteredWithC(_: anytype, renderer: anytype, text: []const u8, r: anytype, scale: i32) void {
    drawTextCentered(renderer, text, r, scale);
}
